package com.kitri.admin.model;

public class PathDto {
	private String path;
	private String contentPath;
	private String headPath;
	private String bottomPath;
	private String titleHead;
	
	public void setPath(String path) {
		this.path = path;
	}
	public void setContentPath(String contentPath) {
		this.contentPath = contentPath;
	}
	public void setHeadPath(String headPath) {
		this.headPath = headPath;
	}
	public void setBottomPath(String bottomPath) {
		this.bottomPath = bottomPath;
	}
	public void setTitleHead(String titleHead) {
		this.titleHead = titleHead;
	}
	public String getPath() {
		return path;
	}
	public String getContentPath() {
		return contentPath;
	}
	public String getHeadPath() {
		return headPath;
	}
	public String getBottomPath() {
		return bottomPath;
	}
	public String getTitleHead() {
		return titleHead;
	}
	
	
}
